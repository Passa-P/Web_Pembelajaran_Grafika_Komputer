import streamlit as st

st.title("Materi 1: Pengantar Grafika Komputer")

st.subheader("Apa itu Grafika Komputer?")
st.write("""
Grafika komputer adalah cabang ilmu komputer yang berfokus pada pembuatan, manipulasi, dan representasi visual dari data secara digital.
""")

st.subheader("Tujuan Grafika Komputer")
st.markdown("""
- Menampilkan informasi secara visual.
- Membantu simulasi dan permodelan.
- Meningkatkan interaksi antara manusia dan komputer.
""")

st.subheader("Contoh Penerapan:")
st.markdown("""
- Animasi dan Film.
- Desain Game.
- Visualisasi Data.
- Aplikasi CAD (Computer-Aided Design).
""")
