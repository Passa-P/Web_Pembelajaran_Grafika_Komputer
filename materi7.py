import streamlit as st

st.title("Materi 7: Teknik Texturing")

st.write("""
Texturing adalah proses memberikan pola atau gambar pada permukaan objek 3D agar tampak realistis.
""")

st.subheader("Jenis Teknik Texturing")
st.markdown("""
- **Texture Mapping:** Menempelkan gambar ke objek 3D.
- **Bump Mapping:** Meniru efek permukaan kasar.
- **Environment Mapping:** Menciptakan efek refleksi.
- **Normal Mapping:** Meningkatkan detail tanpa menambah poligon.
""")

st.info("Teknik texturing banyak digunakan dalam video game, animasi, dan simulasi 3D.")
